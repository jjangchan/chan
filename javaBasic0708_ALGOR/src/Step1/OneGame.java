package Step1;

import java.util.ArrayList;

public class OneGame {
	private int point;
	private String bouns;
	private ArrayList<String> myOption;
	
	public int getPoint() {
		return point;
	}
	public void setPoint(int point) {
		this.point = point;
	}

	public String getBouns() {
	return bouns;
    }
    public void setBouns(String bouns) {
	this.bouns = bouns;
    }
	public ArrayList<String> getMyOption() {
		return myOption;
	}
	public void setMyOption(ArrayList<String> myOption) {
		this.myOption = myOption;
	}

}
